#include <stdio.h>

int main() {

	//типізація даних, масив на 10 елементів і для циклу
	int array[10], i;

	//вивести дані про те що потрібно зробити користувачу
	printf("Enter 10 number:\n");


	//ввід десяти єлементів масиву
	for (i = 0; i < 10; i++) {

		scanf_s("%d", &array[i]);

	}

	//для розділення (кращого читання)
	printf("\n\n");

	//вивід масиву в зворотному порядку
	for (i = 9; i >= 0; i--) {

		printf("%d\n", array[i]);

	}

	//повернути 0
	return 0;
}